# 퀴즈 게임 - 건도가 만들었어요!

# 문제랑 답
문제들 = [
    {"문제": "Python 최신 버전은 3.x? (O/X)", "답": "O"},
    {"문제": "리스트는 순서 없는거야? (O/X)", "답": "X"},
    {"문제": "함수는 def로 만들지? (O/X)", "답": "O"}
]

점수 = 0

# 반복 시작
for 문제 in 문제들:
    답 = input(문제["문제"] + " ")
    if 답.upper() == 문제["답"]:
        print("맞았어요!\n")
        점수 += 1
    else:
        print("아니에요ㅠ 정답은", 문제["답"], "입니다.\n")

print("게임 끝! 점수:", 점수, "/", len(문제들))
